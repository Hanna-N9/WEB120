<p>The main staple food in Eritrea is injera. Injera is made from teff flour, and the fermentation process takes about four to five days before using a skillet. Eritrea has similar foods with Ethiopia. Injera is also the main staple food in Ethiopia. Both countries share the use of berbere (a mixture of 30 different spices) in their foods. Berbere is a lengthy procedure to make, but it is what makes their food spicy. Unlike Ethiopian foods, most Eritrean foods use lighter to no butter. There are more seafood dishes compared to Ethiopia because of its border by the Red Sea. Ethiopian food is mostly onion based whereby Eritrean food has tomatoes and onions. In other words, Eritreans use more tomatoes in their foods. That is, tomatoes are a base for their many dishes, which changes the flavor. The use of tomato as a base is due to the heavy influence of Italy that colonized Eritrea. Eritrean has some fusion dishes that come from Italian cuisine including spaghetti, lasagna, pizza, and desserts.</p>

<p>The coffee ceremony is important to Eritrea and Ethiopia. That is, coffee brings families and guests together. Coffee is served in small cups, and it goes with popcorn and hambesha (bread). The image will be shown what it looks like.</p>

